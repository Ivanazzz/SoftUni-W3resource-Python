# Task 1
# Write a Python program to sum all the items in a list.

list = [1, 2, 3, 4, 5, 6, 7, 8, 9]
total = 0
for i in list:
    total += i
print(total)



# Task 2
# Write a Python program to multiply all the items in a list.

list = [1, 2, 3, 4, 5, 6, 7, 8, 9]
total = 1
for i in list:
    total *= i
print(total)



